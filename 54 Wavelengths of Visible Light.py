#54:Wavelengths of Visible Light
a = float(input("Enter the wavelength:"))
if 380<=a<450:
    print("Color of wavelength is Violet")
elif 450<=a<495:
    print("Color of wavelength is Blue")
elif 495<=a<570:
    print("Color of wavelength is Green")
elif 570<=a<590:
    print("Color of wavelength is Yellow")
elif 590<=a<620:
    print("Color of wavelength is Orange")
elif 620<=a<750:
    print("Color of wavelength is Red")
else:
    print("Wavelength is outside the visible spectrum")

    
